# Esse módulo realiza as quatro operações em matematicas

#Recebe dois números e retorna a soma
def adicao(x,y):
    return x + y
#Recebe dois números e retorna a diferença
def subtracao(x,y):
    return x - y
#Recebe dois números e retorna o produto
def multiplicacao(x,y):
    return x * y
#Recebe dois números e retorna a divisão
def divisao(x,y):
    return x / y